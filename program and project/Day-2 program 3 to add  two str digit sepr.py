# Write a program that adds the digites in a 2 digit number. e.g. if the input was 35, then output should be 3+5=8

two_digit_num = input('Type a two digite number:')

first_digite = int(two_digit_num[0])
second_digite = int(two_digit_num[1])

result = first_digite + second_digite

print(result)